public class BitManiPulation {
    public static void main(String[] args) {
        // Binary AND Operator
        System.out.println(5&6);

        // Binary OR Operator
        System.out.println(5|6);

        // Binary XOR Operator
        System.out.println(5^6);

        // Binary One's Compliment / NOT Operator

        System.out.println(~5);


    }
}
