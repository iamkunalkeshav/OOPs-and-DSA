public class BitManipulation {
    public static void main(String[] args) {

        // AND Operator
        System.out.println(5&6);

        // OR Operator
        System.out.println(5 | 6);

        // XOR Operator
        System.out.println(5 ^ 6);

        // Binary ones's  complement
        System.out.println(~0);
        //BLS
        System.out.println(5<<2);
    }
}

